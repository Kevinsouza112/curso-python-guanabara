# Objetivo: ler o nome da pessoa e mostrar uma mensagem de boas-vindas.

# A funcao input() mostra a pergunta na tela e espera o usuario digitar uma resposta.
# O texto digitado pelo usuario fica guardado dentro da variavel nome.
nome = input('Qual seu nome? ')

# A funcao print() mostra uma mensagem na tela.
# Aqui, ela junta o texto 'Ola', o valor da variavel nome e o restante da frase.
print('Ola', nome, 'Prazer em conhecer!')
